export function t(key: string, langCode: string): string {
  if (!langCode) return TRANSLATIONS['en'][key] || key;
  // Normalize code (e.g. "English" or "en")
  let code = langCode.toLowerCase().substring(0, 2);
  if (langCode.toLowerCase() === 'english') code = 'en';
  if (langCode.toLowerCase() === 'hindi') code = 'hi';
  if (langCode.toLowerCase() === 'tamil') code = 'ta';
  if (langCode.toLowerCase() === 'bengali') code = 'bn';
  if (langCode.toLowerCase() === 'telugu') code = 'te';
  if (langCode.toLowerCase() === 'marathi') code = 'mr';
  if (langCode.toLowerCase() === 'gujarati') code = 'gu';
  if (langCode.toLowerCase() === 'kannada') code = 'kn';
  if (langCode.toLowerCase() === 'malayalam') code = 'ml';
  if (langCode.toLowerCase() === 'punjabi') code = 'pa';

  const dict = TRANSLATIONS[code] || TRANSLATIONS['en'];
  return dict[key] || TRANSLATIONS['en'][key] || key;
}
